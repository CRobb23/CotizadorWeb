    public static BusinessDetailDTO loadData(QueryBusinessDetailResponse businessDetail) {
        BusinessDetailDTO dto = new BusinessDetailDTO();
        dto.setTaxNumber(businessDetail.getTaxNumber());
        dto.setSocietyType(ER_Society_Type.getObjectId(businessDetail.getSocietyType()));
        dto.setCompanyName(businessDetail.getCompanyName());
        dto.setBusinessName(businessDetail.getBusinessName());
        dto.setEconomicActivity(ER_Economic_Activity.getObjectId(businessDetail.getEconomicActivity()));
        dto.setNationality(ER_Nationality.getObjectId(businessDetail.getNationality()));
        dto.setDpiCompany(businessDetail.getDpiCompany());
        dto.setStateProvider(businessDetail.getStateProvider());
        dto.setEmail(businessDetail.getEmail());
        dto.setWriteNumber(businessDetail.getWriteNumber());
        dto.setWriteDate(businessDetail.getWriteDate());
        dto.setRegistrationDate(businessDetail.getRegistrationDate());
        dto.setCondeCifBank(businessDetail.getCondeCifBank());
        dto.setCodeClient(businessDetail.getCodeClient());

        if (businessDetail.getLegalRepresentative() != null) {
            dto.setFirstNameRep(businessDetail.getLegalRepresentative().getFirstName());
            dto.setSecondNameRep(businessDetail.getLegalRepresentative().getSecondName());
            dto.setFirstSurnameRep(businessDetail.getLegalRepresentative().getFirstSurname());
            dto.setSecondSurnameRep(businessDetail.getLegalRepresentative().getSecondSurname());
            dto.setMarriedSurnameRep(businessDetail.getLegalRepresentative().getMarriedSurname());
            dto.setNationalityRep(ER_Nationality.getObjectId(businessDetail.getLegalRepresentative().getNationality()));
            dto.setRegisterRep(businessDetail.getLegalRepresentative().getRegister());
            dto.setCaseFileRep(businessDetail.getLegalRepresentative().getCaseFile());
            dto.setExtendedInRep(businessDetail.getLegalRepresentative().getExtendedIn());
            dto.setRegistrationDateRep(businessDetail.getLegalRepresentative().getRegistrationDate());
            dto.setBirthdateRep(businessDetail.getLegalRepresentative().getBirthdate());
            dto.setProfessionRep(ER_Profession.getObjectId(businessDetail.getLegalRepresentative().getProfession()));
            dto.setBookRep(businessDetail.getLegalRepresentative().getBook());
            dto.setFolioRep(businessDetail.getLegalRepresentative().getFolio());
            dto.setDpiRep(businessDetail.getLegalRepresentative().getDpi());
            dto.setTaxNumberRep(businessDetail.getLegalRepresentative().getTaxNumber());
            dto.setPassportRep(businessDetail.getLegalRepresentative().getPassport());
            dto.setSexRep(ER_Sex.getObjectId(businessDetail.getLegalRepresentative().getSex()));
            dto.setCivilStatusRep(ER_Civil_Status.getObjectId(businessDetail.getLegalRepresentative().getCivilStatus()));
            dto.setEmailRep(businessDetail.getLegalRepresentative().getEmail());
            dto.setMonthlyAmountIncomeRep(businessDetail.getLegalRepresentative().getMonthlyAmountIncome());
            dto.setMonthlyAmountOfIncomeRep(businessDetail.getLegalRepresentative().getMonthlyAmountOfIncome());
        }

        if (businessDetail.getAddress() != null) {
            if (businessDetail.getAddress().getRepresentativeLegalAddress() != null) {
                dto.setAddressLegal(businessDetail.getAddress().getRepresentativeLegalAddress().getAddress());
                dto.setCountryLegal(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getRepresentativeLegalAddress().getCountry()));
                dto.setDepartmentLegal(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getRepresentativeLegalAddress().getDepartment()));
                dto.setMunicipalityLegal(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getRepresentativeLegalAddress().getMunicipality()));
                dto.setZoneLegal(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getRepresentativeLegalAddress().getZone()));
                dto.setPhone1Legal(businessDetail.getAddress().getRepresentativeLegalAddress().getPhone1());
                dto.setPhone2Legal(businessDetail.getAddress().getRepresentativeLegalAddress().getPhone2());
                dto.setPhone3Legal(businessDetail.getAddress().getRepresentativeLegalAddress().getPhone3());
                dto.setColonyLegal(businessDetail.getAddress().getRepresentativeLegalAddress().getColony());
            }
            if (businessDetail.getAddress().getWorkAddress() != null) {
                dto.setAddressWork(businessDetail.getAddress().getWorkAddress().getAddress());
                dto.setCountryWork(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getWorkAddress().getCountry()));
                dto.setDepartmentWork(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getWorkAddress().getDepartment()));
                dto.setMunicipalityWork(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getWorkAddress().getMunicipality()));
                dto.setZoneWork(ER_Geographic_Area.getObjectId(businessDetail.getAddress().getWorkAddress().getZone()));
                dto.setPhone1Work(businessDetail.getAddress().getWorkAddress().getPhone1());
                dto.setPhone2Work(businessDetail.getAddress().getWorkAddress().getPhone2());
                dto.setPhone3Work(businessDetail.getAddress().getWorkAddress().getPhone3());
            }
        }
        return dto;
    }
