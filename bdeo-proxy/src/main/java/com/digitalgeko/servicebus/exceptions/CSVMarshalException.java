package com.digitalgeko.servicebus.exceptions;

public class CSVMarshalException extends Exception {

	public CSVMarshalException(String message) {
		super(message);
	}

	public CSVMarshalException(Throwable cause) {
		super(cause);
	}

	public CSVMarshalException(String message, Throwable cause) {
		super(message, cause);
	}

}
